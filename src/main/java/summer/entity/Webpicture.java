package summer.entity;

public class Webpicture {
	
	private String webpicture_no;
	
	private String webpicture_url;
	
	private String webpicture_name;
	
	public String getWebpicture_no()
	{
		return this.webpicture_no;
	}

	public void setWebpicture_no(String webpicture_no)
	{
		this.webpicture_no=webpicture_no;
	}
	
	public String getWebpicture_url()
	{
		return this.webpicture_url;
	}
	
	public void setWebpicture_url(String webpicture_url)
	{
		this.webpicture_url=webpicture_url;
	}
	
	public String getWebpicture_name()
	{
		return this.webpicture_name;
	}
	
	public void setWebpicture_name(String webpicture_name)
	{
		this.webpicture_name=webpicture_name;
	}
}
