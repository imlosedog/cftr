package summer.entity;


public class Datadictionary {
	
	private String data_no;

	private String data_code;


	private String data_memo;


	private String data_name;
	
	private String data_typecode;



	
	public String getData_no() {
		return this.data_no;
	}

	public void setData_no(String data_no) {
		this.data_no = data_no;
	}

	public String getData_code() {
		return this.data_code;
	}

	public void setData_code(String data_code) {
		this.data_code = data_code;
	}

	public String getData_typecode() {
		return this.data_typecode;
	}

	public void setData_typecode(String data_typecode) {
		this.data_typecode = data_typecode;
	}
	
	public String getData_memo() {
		return this.data_memo;
	}

	public void setData_memo(String data_memo) {
		this.data_memo = data_memo;
	}

	public String getData_name() {
		return this.data_name;
	}

	public void setData_name(String data_name) {
		this.data_name = data_name;
	}

	

}